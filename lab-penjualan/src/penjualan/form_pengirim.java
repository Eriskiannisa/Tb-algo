    private void tabelpengirimMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelpengirimMouseClicked
        // TODO add your handling code here:
        int baris = tabelpengirim.rowAtPoint(evt.getPoint());
        String no = tabelpengirim.getValueAt(baris, 0).toString();
        txtno.setText(no);
        
        String nama = tabelpengirim.getValueAt(baris, 1).toString();
        txtnama.setText(nama);
        
        String alamat = tabelpengirim.getValueAt(baris, 2).toString();
        txtalamat.setText(alamat);
        
        String kota = tabelpengirim.getValueAt(baris, 3).toString();
        txtkota.setText(kota);
        
        String provinsi = tabelpengirim.getValueAt(baris, 4).toString();
        txtprovinsi.setText(provinsi);
        
        String kabupaten = tabelpengirim.getValueAt(baris, 5).toString();
        txtkab.setText(kabupaten);
        
        String kode = tabelpengirim.getValueAt(baris, 6).toString();
        txtkodepos.setText(kode);
        
        String telepon = tabelpengirim.getValueAt(baris, 7).toString();
        txttelepon.setText(telepon);
    }//GEN-LAST:event_tabelpengirimMouseClicked

